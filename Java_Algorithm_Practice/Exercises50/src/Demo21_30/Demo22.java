public class Demo22 {
    public static void main(String[] args) {
        System.out.println("5的阶乘是：" + JieCheng(5));
    }

    private static int JieCheng(int i) {
        if (i == 1) {
            return 1;
        } else {
            return i * JieCheng(i - 1);
        }
    }
}

