public class Demo27 {
    public static void main(String[] args) {
        int w = 1;
        for (int x = 2; x <= 100; x++) {
            for (int y = 2; y < x; y++) {
                w = x % y;
                if (w == 0)
                    break;
            }
            if (w != 0) {
                System.out.print(x + " ");
            }
        }
    }
}

