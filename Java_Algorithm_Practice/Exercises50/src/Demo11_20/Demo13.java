/*
【程序13】   题目：一个整数，它加上100后是一个完全平方数，加上168又是一个完全平方数，
              请问该数是多少？
 */
public class Demo13 {
    public static void main(String[] args) {
        for (int x = 1; x < 10000; x++) {
            if (Math.sqrt(x + 100) % 1 == 0)
                if (Math.sqrt(x + 168) % 1 == 0) {
                    System.out.println(x);
                }
        }
    }
}

