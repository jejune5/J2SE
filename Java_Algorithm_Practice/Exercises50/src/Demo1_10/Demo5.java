    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("请输入你的成绩：");
        int n = sc.nextInt();
        System.out.println(n >= 90 ? "A" : (n >= 60 ? "B" : "C"));
    }
